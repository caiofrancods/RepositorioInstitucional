      <h3> Qual é o objetivo da Iniciação Científica? </h3>
      <p> O objetivo do Programa Institucional de Iniciação Científica e Tecnológica do CEFET-MG é promover o desenvolvimento do pensamento científico e envolver os estudantes do Ensino Superior e do Ensino Técnico de Nível Médio na prática da pesquisa e inovação. A Iniciação Científica é vista como uma ferramenta formativa que permite aos alunos se engajarem na pesquisa e ter um contato direto com atividades científicas. </p>
      <h3> Como funciona a Iniciação Científica no CFET-MG?</h3>
      <p> Em 2004, o CEFET-MG deu início a esse programa com a concessão de 15 bolsas de Iniciação Científica pelo CNPq e 22 bolsas de Iniciação Científica Júnior pela FAPEMIG. Desde então, a instituição tem investido em fortalecer a participação dos alunos nessa atividade de pesquisa. Atualmente, o programa é apoiado por recursos do CNPq, da FAPEMIG e da própria instituição, sendo dividido em várias modalidades: Programa Institucional de Bolsa de Iniciação Científica (PIBIC) e Programa Institucional de Bolsa de Iniciação em Desenvolvimento Tecnológico e Inovação (PIBITI) para estudantes de graduação, e o Programa Institucional de Bolsa de Iniciação Científica Júnior (PIBIC-Jr) para alunos do Ensino Técnico de Nível Médio do CEFET-MG, bem como alguns alunos de escolas estaduais de Belo Horizonte. <br> <br>

As bolsas oferecidas pelas instituições e agências de fomento são disponibilizadas por meio de editais específicos que convocam propostas para a seleção de bolsistas. Essas propostas são avaliadas levando em consideração o perfil e o desempenho acadêmico dos alunos, bem como o mérito científico dos projetos. A Comissão de Iniciação Científica, composta por especialistas com experiência na seleção e avaliação de projetos, é responsável por essa avaliação.





